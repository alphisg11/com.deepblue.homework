package org.deepblue.javbase.conatiner.Homework;

public class User {

	private String userName;
	private String userPaswsword;
	private String userInformation;
	
	public User() {
		
	}

	public User(String userName, String userPaswsword, String userInformation) {
		this.userName = userName;
		this.userPaswsword = userPaswsword;
		this.userInformation = userInformation;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserPaswsword() {
		return userPaswsword;
	}

	public void setUserPaswsword(String userPaswsword) {
		this.userPaswsword = userPaswsword;
	}

	public String getUserInformation() {
		return userInformation;
	}

	public void setUserInformation(String userInformation) {
		this.userInformation = userInformation;
	}

}
